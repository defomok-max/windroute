#!/usr/bin/env node
/**
 * windbu CLI entrypoint.
 *
 * Usage:
 *   windbu                 — start the gateway (auto-configures on first run)
 *   windbu start           — same as plain `windbu`
 *   windbu install         — run the interactive installer (ls detect, .env, autostart)
 *   windbu stop            — stop any running windbu process
 *   windbu login <token>   — add a Windsurf token to the pool
 *   windbu dashboard       — open the web dashboard in the default browser
 *   windbu doctor          — preflight checks (node, port, data dir, LS binary)
 *   windbu version         — print version
 *   windbu help            — show this help
 *
 * Design goals:
 *   - Zero configuration for the "I just installed this" case — start the
 *     gateway, auto-detect the LS binary, generate secrets, print the URL.
 *   - Sane defaults for every interactive prompt so the happy path is
 *     "press enter, press enter, press enter".
 *   - Cross-platform: runs on Windows (primary), macOS, Linux (LS must be
 *     on-disk; on non-Windows we can still run the dashboard + API without
 *     chat by pointing LS_BINARY_PATH at a compatible binary).
 */

import { fileURLToPath } from 'url';
import { dirname, join, resolve } from 'path';
import { existsSync, readFileSync, writeFileSync, mkdirSync, unlinkSync } from 'fs';
import { spawn, spawnSync } from 'child_process';
import { randomBytes } from 'crypto';
import { homedir, platform } from 'os';
import http from 'http';
import net from 'net';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PKG_ROOT = resolve(__dirname, '..');
const PKG_JSON = JSON.parse(readFileSync(join(PKG_ROOT, 'package.json'), 'utf-8'));

const DEFAULT_PORT = 20129;
const DATA_DIR = process.env.WINDBU_DATA_DIR
  || (platform() === 'win32'
    ? join(process.env.USERPROFILE || homedir(), '.windbu')
    : join(homedir(), '.windbu'));

// ─── UI helpers ─────────────────────────────────────────────
const c = {
  reset: '\x1b[0m', bold: '\x1b[1m', dim: '\x1b[2m',
  red: '\x1b[31m', green: '\x1b[32m', yellow: '\x1b[33m',
  blue: '\x1b[34m', magenta: '\x1b[35m', cyan: '\x1b[36m',
};
const isTty = process.stdout.isTTY;
const paint = (color, s) => isTty ? `${c[color]}${s}${c.reset}` : s;
const step = (msg) => console.log(`${paint('cyan', '›')} ${msg}`);
const ok = (msg) => console.log(`  ${paint('green', '✓')} ${msg}`);
const warn = (msg) => console.log(`  ${paint('yellow', '!')} ${msg}`);
const fail = (msg) => console.log(`  ${paint('red', '✗')} ${msg}`);

function banner() {
  const v = PKG_JSON.version || '1.0.0';
  console.log(`
${paint('cyan', '  ╭─────────────────────────────────────────╮')}
${paint('cyan', '  │')}  ${paint('bold', 'windbu')} ${paint('dim', 'v' + v)}                           ${paint('cyan', '│')}
${paint('cyan', '  │')}  local gateway for Windsurf AI          ${paint('cyan', '│')}
${paint('cyan', '  │')}  OpenAI + Anthropic compatible          ${paint('cyan', '│')}
${paint('cyan', '  ╰─────────────────────────────────────────╯')}
`);
}

// ─── .env handling ─────────────────────────────────────────
function envPath() {
  return join(PKG_ROOT, '.env');
}
function readEnv() {
  const p = envPath();
  if (!existsSync(p)) return {};
  const out = {};
  for (const line of readFileSync(p, 'utf-8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const i = t.indexOf('=');
    if (i === -1) continue;
    const k = t.slice(0, i).trim();
    let v = t.slice(i + 1).trim();
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1);
    out[k] = v;
  }
  return out;
}
function writeEnvFile(obj) {
  const lines = [
    `# windbu configuration (generated by windbu CLI)`,
    `PORT=${obj.PORT || DEFAULT_PORT}`,
    `HOST=${obj.HOST || '127.0.0.1'}`,
    `API_KEY=${obj.API_KEY || ''}`,
    `DASHBOARD_PASSWORD=${obj.DASHBOARD_PASSWORD || ''}`,
    `LS_BINARY_PATH=${obj.LS_BINARY_PATH || ''}`,
    `LS_PORT=${obj.LS_PORT || 42100}`,
    `DEFAULT_MODEL=${obj.DEFAULT_MODEL || 'claude-sonnet-4.6'}`,
    `MAX_TOKENS=${obj.MAX_TOKENS || 8192}`,
    `LOG_LEVEL=${obj.LOG_LEVEL || 'info'}`,
    `CODEIUM_API_URL=${obj.CODEIUM_API_URL || 'https://server.self-serve.windsurf.com'}`,
  ];
  writeFileSync(envPath(), lines.join('\n') + '\n', 'utf-8');
}

function randomHex(bytes) {
  return randomBytes(bytes).toString('hex');
}
function randomPassword(len = 16) {
  const alphabet = 'abcdefghijkmnpqrstuvwxyzABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  const b = randomBytes(len);
  let out = '';
  for (const v of b) out += alphabet[v % alphabet.length];
  return out;
}

// ─── LS binary detection ───────────────────────────────────
function detectLsBinary() {
  if (platform() === 'win32') {
    const candidates = [
      process.env.LOCALAPPDATA && join(process.env.LOCALAPPDATA, 'Programs', 'Windsurf', 'resources', 'app', 'extensions', 'windsurf', 'bin', 'language_server_windows_x64.exe'),
      process.env.APPDATA && join(process.env.APPDATA, 'Windsurf', 'bin', 'language_server_windows_x64.exe'),
      process.env.LOCALAPPDATA && join(process.env.LOCALAPPDATA, 'Windsurf', 'bin', 'language_server_windows_x64.exe'),
      process.env.ProgramFiles && join(process.env.ProgramFiles, 'Windsurf', 'resources', 'app', 'extensions', 'windsurf', 'bin', 'language_server_windows_x64.exe'),
      join(DATA_DIR, 'ls', 'language_server_windows_x64.exe'),
    ].filter(Boolean);
    for (const c of candidates) if (existsSync(c)) return c;
    return null;
  }
  // macOS / Linux: the LS ships inside the Windsurf desktop app bundle.
  // These paths are best-effort; users on non-Windows typically provide
  // LS_BINARY_PATH themselves.
  const arch = process.arch === 'arm64' ? 'arm' : 'x64';
  if (platform() === 'darwin') {
    const p = `/Applications/Windsurf.app/Contents/Resources/app/extensions/windsurf/bin/language_server_macos_${arch}`;
    if (existsSync(p)) return p;
  }
  if (platform() === 'linux') {
    const p = `/opt/Windsurf/resources/app/extensions/windsurf/bin/language_server_linux_${arch}`;
    if (existsSync(p)) return p;
  }
  return null;
}

// ─── First-run auto-config ─────────────────────────────────
function ensureConfigured() {
  const envFile = envPath();
  if (existsSync(envFile)) {
    const env = readEnv();
    if (env.API_KEY && env.DASHBOARD_PASSWORD) return env;
    // Incomplete .env — fall through and rewrite with defaults.
  }
  step('First run detected — auto-configuring');
  mkdirSync(DATA_DIR, { recursive: true });
  const apiKey = 'sk-windbu-' + randomHex(16);
  const password = randomPassword(16);
  const ls = detectLsBinary();
  if (ls) ok(`Language Server detected: ${paint('dim', ls)}`);
  else warn('Language Server not found. Add one via `windbu install` or edit .env');
  const env = {
    ...readEnv(),
    PORT: String(DEFAULT_PORT),
    HOST: '127.0.0.1',
    API_KEY: apiKey,
    DASHBOARD_PASSWORD: password,
    LS_BINARY_PATH: ls || '',
    LS_PORT: '42100',
    DEFAULT_MODEL: 'claude-sonnet-4.6',
    MAX_TOKENS: '8192',
    LOG_LEVEL: 'info',
    CODEIUM_API_URL: 'https://server.self-serve.windsurf.com',
  };
  writeEnvFile(env);
  ok(`.env written: ${paint('dim', envFile)}`);

  // Persist credentials so users can find them later.
  const credPath = join(DATA_DIR, 'credentials.txt');
  const cred = [
    `windbu credentials`,
    `Generated: ${new Date().toISOString()}`,
    ``,
    `Dashboard:  http://127.0.0.1:${DEFAULT_PORT}/dashboard`,
    `Password:   ${password}`,
    ``,
    `API base:   http://127.0.0.1:${DEFAULT_PORT}/v1`,
    `API key:    ${apiKey}`,
    ``,
    `Config:     ${envFile}`,
  ].join('\n') + '\n';
  try { writeFileSync(credPath, cred, 'utf-8'); ok(`Credentials saved: ${paint('dim', credPath)}`); } catch {}
  return env;
}

// ─── Subcommands ──────────────────────────────────────────
function cmdStart() {
  banner();
  const env = ensureConfigured();
  step(`Starting windbu on http://${env.HOST || '127.0.0.1'}:${env.PORT || DEFAULT_PORT}`);
  const entry = join(PKG_ROOT, 'src', 'index.js');
  const child = spawn(process.execPath, [entry], {
    cwd: PKG_ROOT, stdio: 'inherit', env: { ...process.env, ...env },
  });
  child.on('exit', (code) => process.exit(code ?? 0));
  child.on('error', (err) => { fail(`Failed to start: ${err.message}`); process.exit(1); });

  // Open the browser a few seconds after boot — only on TTY + non-CI so
  // unattended/systemd launches don't try to `xdg-open` a browser. Accept
  // either `--no-open` on the command line or WINDBU_NO_BROWSER in the env
  // to suppress the popup.
  const port = parseInt(env.PORT || DEFAULT_PORT, 10);
  const wantsNoOpen = process.argv.includes('--no-open') || process.argv.includes('--no-browser');
  const shouldOpen = isTty && !process.env.CI && !wantsNoOpen && !process.env.WINDBU_NO_BROWSER;
  if (shouldOpen) {
    setTimeout(() => probeHealth(port).then((ok) => {
      if (ok) openBrowser(`http://127.0.0.1:${port}/dashboard`);
    }).catch(() => {}), 3000);
  }
}

function cmdStop() {
  banner();
  step('Stopping windbu');
  const pidFile = join(DATA_DIR, 'windbu.pid');
  if (existsSync(pidFile)) {
    try {
      const pid = parseInt(readFileSync(pidFile, 'utf-8').trim(), 10);
      if (Number.isFinite(pid)) {
        if (platform() === 'win32') spawnSync('taskkill', ['/PID', String(pid), '/T', '/F'], { stdio: 'ignore' });
        else { try { process.kill(pid, 'SIGTERM'); } catch {} }
        ok(`Stopped PID ${pid}`);
      }
    } catch {}
  } else {
    warn('No PID file found — nothing to stop. Close the gateway window manually.');
  }
}

async function cmdLogin(token) {
  if (!token) { fail('Usage: windbu login <token>'); process.exit(1); }
  const env = readEnv();
  const port = parseInt(env.PORT || DEFAULT_PORT, 10);
  banner();
  step(`Adding token to the pool (http://127.0.0.1:${port}/auth/login)`);
  const body = JSON.stringify({ token });
  // Include the gateway's own credentials so /auth/login passes the
  // admin-auth gate added in the security audit. Prefer the API key; fall
  // back to DASHBOARD_PASSWORD so either one works.
  const headers = { 'Content-Type': 'application/json' };
  if (env.API_KEY) headers['Authorization'] = `Bearer ${env.API_KEY}`;
  if (env.DASHBOARD_PASSWORD) headers['X-Dashboard-Password'] = env.DASHBOARD_PASSWORD;
  let res;
  try {
    res = await fetch(`http://127.0.0.1:${port}/auth/login`, {
      method: 'POST', headers, body,
    });
  } catch (e) {
    fail(`windbu isn't running on port ${port}. Start it with \`windbu\`.`);
    process.exit(1);
  }
  const data = await res.json().catch(() => ({}));
  if (res.ok && data.success) {
    ok(`Account added: ${data.account?.email} (id=${data.account?.id})`);
    console.log(`  pool: ${data.total} total, ${data.active} active`);
  } else {
    fail(`Login failed: ${data.error || res.statusText}`);
    process.exit(1);
  }
}

async function cmdDashboard() {
  const env = readEnv();
  const port = parseInt(env.PORT || DEFAULT_PORT, 10);
  const url = `http://127.0.0.1:${port}/dashboard`;
  step(`Opening ${url}`);
  openBrowser(url);
}

async function cmdDoctor() {
  banner();
  let bad = 0;
  const env = readEnv();
  const port = parseInt(env.PORT || DEFAULT_PORT, 10);

  step('Node version');
  const major = parseInt(process.versions.node.split('.')[0], 10);
  if (major >= 20) ok(`node ${process.versions.node}`);
  else { fail(`node ${process.versions.node} — need >=20`); bad++; }

  step('Data directory');
  try {
    mkdirSync(DATA_DIR, { recursive: true });
    const probe = join(DATA_DIR, '.write-probe');
    writeFileSync(probe, 'ok');
    try { unlinkSync(probe); } catch {}
    ok(`${DATA_DIR} writable`);
  } catch (e) { fail(`Data dir not writable: ${e.message}`); bad++; }

  step('.env');
  if (existsSync(envPath())) ok(`present with ${Object.keys(env).length} keys`);
  else { warn('missing — will be generated on first run'); }

  step('Language Server');
  const ls = env.LS_BINARY_PATH || detectLsBinary();
  if (ls && existsSync(ls)) ok(`found: ${ls}`);
  else { warn('not found — chat requires LS. Install Windsurf or set LS_BINARY_PATH.'); }

  step('Port availability');
  const inUse = await probePort(port);
  if (!inUse) ok(`port ${port} free`);
  else {
    const healthy = await probeHealth(port);
    if (healthy) ok(`port ${port} is serving a windbu instance`);
    else { fail(`port ${port} in use by something else`); bad++; }
  }
  console.log();
  if (bad) { console.log(paint('red', `${bad} issue(s) need fixing`)); process.exit(1); }
  console.log(paint('green', 'All checks passed.'));
}

function cmdVersion() {
  console.log(PKG_JSON.version || 'unknown');
}

function cmdInstall() {
  banner();
  step('Running installer');
  if (platform() === 'win32') {
    const ps1 = join(PKG_ROOT, 'scripts', 'install.ps1');
    const res = spawnSync('powershell.exe', ['-NoLogo', '-NoProfile', '-ExecutionPolicy', 'Bypass', '-File', ps1], { stdio: 'inherit' });
    process.exit(res.status || 0);
  } else {
    // Non-Windows: run the JS-native auto-config.
    ensureConfigured();
    ok('Setup complete. Start with: windbu');
  }
}

function cmdHelp() {
  banner();
  console.log(`${paint('bold', 'Usage:')}  windbu [command]`);
  console.log();
  console.log(`  ${paint('cyan', 'windbu')}                   start the gateway (auto-configures)`);
  console.log(`  ${paint('cyan', 'windbu start')}             same as \`windbu\``);
  console.log(`  ${paint('cyan', 'windbu install')}           run the interactive installer`);
  console.log(`  ${paint('cyan', 'windbu stop')}              stop any running windbu process`);
  console.log(`  ${paint('cyan', 'windbu login')} <token>      add a Windsurf token`);
  console.log(`  ${paint('cyan', 'windbu dashboard')}         open the dashboard in a browser`);
  console.log(`  ${paint('cyan', 'windbu doctor')}            run preflight checks`);
  console.log(`  ${paint('cyan', 'windbu version')}           print the version`);
  console.log();
  console.log(`Data dir:  ${paint('dim', DATA_DIR)}`);
  console.log(`Config:    ${paint('dim', envPath())}`);
}

// ─── utilities ─────────────────────────────────────────────
function probePort(port) {
  return new Promise((resolvePromise) => {
    const srv = net.createServer();
    srv.once('error', () => resolvePromise(true));
    srv.once('listening', () => { srv.close(() => resolvePromise(false)); });
    srv.listen(port, '127.0.0.1');
  });
}

function probeHealth(port, attempts = 8) {
  return new Promise((resolvePromise) => {
    let tries = 0;
    const tick = () => {
      tries++;
      const req = http.request({
        host: '127.0.0.1', port, path: '/health', method: 'GET', timeout: 1000,
      }, (res) => {
        let body = '';
        res.on('data', d => body += d);
        res.on('end', () => {
          try { const j = JSON.parse(body); resolvePromise(j.status === 'ok'); }
          catch { resolvePromise(false); }
        });
      });
      req.on('error', () => tries < attempts ? setTimeout(tick, 500) : resolvePromise(false));
      req.on('timeout', () => { req.destroy(); tries < attempts ? setTimeout(tick, 500) : resolvePromise(false); });
      req.end();
    };
    tick();
  });
}

function openBrowser(url) {
  try {
    if (platform() === 'win32') spawn('cmd', ['/c', 'start', '', url], { detached: true, stdio: 'ignore' }).unref();
    else if (platform() === 'darwin') spawn('open', [url], { detached: true, stdio: 'ignore' }).unref();
    else spawn('xdg-open', [url], { detached: true, stdio: 'ignore' }).unref();
  } catch {}
}

// ─── entrypoint ────────────────────────────────────────────
const [, , ...args] = process.argv;
const cmd = args[0] || 'start';
switch (cmd) {
  case 'start': cmdStart(); break;
  case 'install': cmdInstall(); break;
  case 'stop': cmdStop(); break;
  case 'login': cmdLogin(args[1]); break;
  case 'dashboard': case 'dash': case 'open': cmdDashboard(); break;
  case 'doctor': case 'check': cmdDoctor(); break;
  case 'version': case '-v': case '--version': cmdVersion(); break;
  case 'help': case '-h': case '--help': cmdHelp(); break;
  default:
    fail(`Unknown command: ${cmd}`);
    cmdHelp();
    process.exit(1);
}
